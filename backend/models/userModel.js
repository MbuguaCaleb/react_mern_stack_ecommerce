import mongoose from 'mongoose'

//Structure of the Collection
const userSchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    isAdmin: {
      type: Boolean,
      required: true,
      default: false,
    },
  },
  { timeStamps: true }
)

//creating the collection itself
const User = mongoose.model('User', userSchema)

export default User
